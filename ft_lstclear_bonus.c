#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*aux;

	if (!lst)
		return ;
	while (*lst)
	{
		del((*lst)->content);
		aux = (*lst)->next;
		free(*lst);
		*lst = aux;
	}
}
